function subtract(x, y) {
    var result = x - y;
    return result;
}

function add(x, y) {
    var result = x + y;
    return result;
}

function multiply(x, y) {
    var result = x * y;
    return result;
}

function divide(x, y) {
    var result = x / y;
    return result;
}

function remainder(x, y) {
    var result = x % y;
    return result;
}
